package com.jekdev.saappapi.base;

/**
 * Represents different types of emotions that can be associated with a client or an entity. The enumeration provides
 * three levels of emotional states:
 * <p>
 * - HAPPY: Represents a positive or joyful emotional state. - MIDDLE: Represents a neutral or balanced emotional state.
 * - BAD: Represents a negative or unfavorable emotional state. This enum can be used to categorize and manage the
 * emotional status of entities in various application contexts, such as client interactions or user feedback systems.
 */
public enum EmotionType {
    HAPPY, MIDDLE, BAD
}
